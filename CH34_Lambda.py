# Defining a lambda function 'double' that doubles the input
double = lambda x: x * 2

# Defining a lambda function 'cube' that calculates the cube of the input
cube = lambda x: x * x * x

# Defining a lambda function 'avg' that calculates the average of three inputs
avg = lambda x, y, z: (x + y + z) / 3

# Printing the results of using the lambda functions
print(double(2))  # Result of doubling 2: 4
print(cube(5))     # Result of cubing 5: 125
print(avg(5, 7, 6))  # Result of averaging 5, 7, and 6: 6.0
