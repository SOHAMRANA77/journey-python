# variables = container that hold the address of value

# Integer variable
a = 1
print(a)
print(type(a))

# String variable
b = "1 Sohyem"
print(b)
print(type(b))

# Concatenating integer and string, and printing the result
print("a + b = ", str(a) + b)

# Boolean variable
c = True
print(c)
print(type(c))

# NoneType variable
d = None
print(d)
print(type(d))

# Complex number variable
e = complex(4, 22)
print(e)
print(type(e))

# List variable
List = [8, 2, 3.2, [-4, 5], ["apple", "banna"]]
print(List)
print(type(List))

# Tuple variable
Tuple1 = (1, 2, "soham", -1, 33.4)
print(Tuple1)
print(type(Tuple1))

# Dictionary variable
Dict1 = {1: "soham", 2: "harnisha", 3: "joohi", 4: "pallavi", 5: "sharmistha", 6: "harish", "age": 23, "can vote": True}
print(Dict1)
print(type(Dict1))

# Accessing a specific value from the dictionary
print(Dict1[1])

