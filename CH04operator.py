# Arithmetic operations on numbers

# Addition
print(5 + 6)

# Division
print(5 / 6)

# Subtraction
print(5 - 6)

# Multiplication
print(5 * 6)

# Modulus (remainder of the division)
print(5 % 6)

# Floor division (integer division, discarding the fractional part)
print(5 // 6)

# Exponentiation (5 raised to the power of 6)
print(5 ** 6)

